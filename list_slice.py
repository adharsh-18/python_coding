#list creation
l = ["USA","INDIA","BRAZIL","ARGENTINA","CANADA","AUSTRALIA","SOUTH AFRICA","UK"]

#for slicing use list_name[start_index:end_index:step_index]

#straight slicing
print(l[2:6:1])
print(l[:4:1]) #blank values are taken to be the first or last

#reverse slicing

print(l[-1::-1])
print(l[::-1])
